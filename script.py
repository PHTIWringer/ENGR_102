def celsius_to_fahrenheit(t):
    '''Temperature Conversion Function'''
    fahrenheit = t * (9/5) + 32
    return fahrenheit

print(celsius_to_fahrenheit(1))
print(celsius_to_fahrenheit(4))
print(celsius_to_fahrenheit(-4))
print(celsius_to_fahrenheit(-1))
